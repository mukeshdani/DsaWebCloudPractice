//ABSTRACT DATA TYPE: Hiding the implementation details of a data structure
//ABSTRACT DATA TYPE: A data type whose implementation is hidden from the user

//Data Type: A set of values and a set of operations on those values

//Primitives: The basic data types provided by the programming language
//Example: int, char, float, double, boolean

//Non-primitive: The data types that are built using the primitive data types
//Example: String, Array, List, Set, Map

//Other Examples of Abstract Data Types:
//1. Stack: A collection of elements with two main operations: push and pop
//2. Queue: A collection of elements with two main operations: enqueue and dequeue
//3. List: A collection of elements that can be accessed by index
//4. Set: A collection of elements that cannot contain duplicates
//5. Map: A collection of key-value pairs
//6. Graph: A collection of nodes and edges
//7. Tree: A hierarchical structure with a root node and child nodes
//8. Hash Table: A collection of key-value pairs that uses a hash function to compute an index into an array of buckets or slots
//9. Priority Queue: A collection of elements with a priority assigned to each element
//10. Heap: A special tree-based data structure that satisfies the heap property
//11. Linked List: A linear collection of data elements, where each element points to the next
//12. Binary Tree: A tree data structure in which each node has at most two children
//13. AVL Tree: A self-balancing binary search tree
//14. Red-Black Tree: A self-balancing binary search tree with an additional property of color
//15. B-Tree: A self-balancing tree data structure that maintains sorted data and allows searches, sequential access, insertions, and deletions in logarithmic time
//16. Trie: A tree-like data structure that stores a dynamic set of strings
//17. Segment Tree: A tree data structure used for storing intervals or segments
//18. Fenwick Tree: A data structure that provides efficient methods for calculation and manipulation of the prefix sums of a table of values
//19. Disjoint Set: A data structure that keeps track of a set of elements partitioned into a number of disjoint (non-overlapping) subsets
//20. Sparse Table: A data structure that allows answering range queries in logarithmic time
//21. LRU Cache: A data structure that removes the least recently used items first
//22. Bloom Filter: A space-efficient probabilistic data structure used to test whether an element is a member of a set
//23. Skip List: A data structure that allows for fast search within an ordered sequence of elements
//24. Suffix Tree: A compressed trie containing all the suffixes of a given text
//25. Suffix Array: A sorted array of all suffixes of a given text
//26. K-D Tree: A space-partitioning data structure for organizing points in a k-dimensional space
//27. Quad Tree: A tree data structure in which each internal node has exactly four children
//28. Octree: A tree data structure in which each internal node has exactly eight children
//29. R-Tree: A tree data structure used for spatial access methods, i.e., for indexing multi-dimensional information
//30. KD-Tree: A space-partitioning data structure for organizing points in a k-dimensional space
//31. Interval Tree: A tree data structure that holds intervals and allows efficient querying of all intervals that overlap with a given interval
//32. Treap: A randomized binary search tree that maintains the heap property
//33. Cartesian Tree: A binary tree derived from a sequence of numbers by inserting the numbers into the tree in a specific order
